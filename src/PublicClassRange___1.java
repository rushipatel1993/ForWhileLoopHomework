import java.util.Scanner;

public class PublicClassRange___1 {

    public static void main(String[] args) {    // scanner method add
        Scanner scanner = new Scanner(System.in);
        System.out.println(" Enter starting value a\t : ");
        int a = scanner.nextInt();
        System.out.println(" Enter starting value b\t : ");
        int b = scanner.nextInt();

        for (int i = a; i <= b; i++) {                     // for loop method

            System.out.println(i);                        // print the main output

        }
    }
}















